class Itinerary {
  final int id;
  final String time;
  final String title;
  final String subtitle;
  final String image;
  final String distance;
  final String duration;
  final String cost;
  final String category;

  Itinerary({
    required this.id,
    required this.time,
    required this.title,
    required this.subtitle,
    required this.image,
    required this.distance,
    required this.duration,
    required this.cost,
    required this.category,
  });
}
